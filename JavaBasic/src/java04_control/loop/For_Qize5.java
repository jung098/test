public class For_Qize5 {

	public static void main(String[] args) {

		
		
		System.out.println("Q5");
		
		for (int i = 1 ; i<=5; i++) {
			
		
			for (int j = 1 ; j<=5;j++) {
				System.out.print(j);
				
			}
			System.out.println("");
			
		}
		
		
		
		
		
	
	}

}
