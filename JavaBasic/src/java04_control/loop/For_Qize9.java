package java04_control.loop;

public class For_Qize9 {

	public static void main(String[] args) {

		System.out.println("Q9");
		int i=0;
		int j=0;
		for (i = 0 ; i<5; i++) {
			
			
			for (j = i ; j<5;j++) {
				System.out.print("*");
			}
			
			System.out.println();
		
		}
		
		
	}
	

}
