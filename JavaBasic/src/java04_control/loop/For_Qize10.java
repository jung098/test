package java04_control.loop;

public class For_Qize10 {

	public static void main(String[] args) {

		//Quiz. 2~9단 구구단 출력
		
		System.out.println("Q1");
		for (int i = 0 ; i<5; i++) {
			System.out.print("*");
		}
		System.out.println("");
		
		
		System.out.println("Q2");

		for (int i = 0 ; i<5; i++) {
			System.out.println("*");
		}
		
		
		System.out.println("");
		
		System.out.println("Q3");

		for (int i = 0 ; i<5; i++) {
			System.out.println("");
			for (int j = 0 ; j<5;j++) {
				System.out.print("*");	
			}
			
		}
		
		System.out.println("");
		
		System.out.println("Q4");
		
		
		for (int i = 1 ; i<=5; i++) {
			System.out.println(i);
			for (int j = 0 ; j<5;j++) {
				System.out.print(i);	
			}
			
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	
	}

}
